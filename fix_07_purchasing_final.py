def fix_inventory_page():
    path = FRONTEND + "/pages/InventoryPage.jsx"
    print("\n[InventoryPage.jsx]")

    src = read(path)

    # ازالة تاب الموردين من الـ tabs array لو موجود
    pattern = r",?\s*\{[^}]*key:\s*['\"]suppliers['\"][^}]*\}"
    if re.search(pattern, src):
        backup(path)
        src = re.sub(pattern, "", src, count=1)
        write(path, src)
        print("  OK: removed suppliers tab entry")
    else:
        print("  SKIP: suppliers tab not found in tabs array")

    # ازالة render لـ SuppliersPanel لو موجود
    src = read(path)
    for candidate in [
        "{tab==='suppliers' && <SuppliersPanel />}",
        "{tab === 'suppliers' && <SuppliersPanel />}",
        "tab === 'suppliers' && <SuppliersPanel",
    ]:
        if candidate in src:
            if not src.startswith("# backed up"):
                backup(path)
            src = src.replace(candidate, "", 1)
            write(path, src)
            print("  OK: removed SuppliersPanel render: " + candidate)
            break
    else:
        print("  INFO: SuppliersPanel render not found (may already be removed)")

