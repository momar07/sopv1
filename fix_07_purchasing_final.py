# ── Helpers ────────────────────────────────────────────────
def abort(msg):
    print("ABORT: " + msg); sys.exit(1)

def backup(path):
    bak = path + ".bak_" + STAMP
    shutil.copy2(path, bak)
    print("  backup -> " + bak)

def read(path):
    with open(path, "r", encoding="utf-8", errors="replace") as f:
        return f.read()

def write(path, content):
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)

def apply_fix(label, path, old, new):
    if not os.path.exists(path):
        abort(path + " not found")
    src = read(path)
    if old not in src:
        print("  SKIP (pattern not found): " + label)
        return False
    backup(path)
    write(path, src.replace(old, new, 1))
    print("  OK: " + label)
    return True

def update_changelog(entry):
    if not os.path.exists(BASE + "/pos_backend/CHANGELOG.md"):
        return
    src = read(BASE + "/pos_backend/CHANGELOG.md")
    if entry[:30] in src:
        return
    write(BASE + "/pos_backend/CHANGELOG.md", entry + "\n" + src)

